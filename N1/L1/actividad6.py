def suma(num1: int, num2: int) -> int:
    '''
    
    Parametros
    ----------
    num1 : int
        El primer numero entero.
    num2 : int
        El segundo numero entero.

    Returns
    -------
    int
        Devuelve la suma de dos enteros.

    '''
    return num1 + num2

